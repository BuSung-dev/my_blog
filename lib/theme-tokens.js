export const FONT_MAP = {
  default: `Pretendard, "Roboto", sans-serif`,
  a2z: `"A2z", "Roboto", sans-serif`,
  roboto: `"Roboto", sans-serif`,
  pretendard: `Pretendard, "Roboto", sans-serif`,
  nanumSquareNeo: `"NanumSquareNeo", "Roboto", sans-serif`
};

export function applyThemeTokens(root, theme, accent, font) {
  const paletteGroup = THEME_TOKENS[accent] ?? THEME_TOKENS.blue;
  const palette = paletteGroup[theme] ?? paletteGroup.light;

  Object.entries(palette).forEach(([token, value]) => {
    root.style.setProperty(token, value);
  });

  root.style.setProperty("--font-family-base", FONT_MAP[font] ?? FONT_MAP.default);
}
